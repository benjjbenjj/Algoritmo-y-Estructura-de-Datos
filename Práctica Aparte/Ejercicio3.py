# Ejercicio 3: Calculador de Tarifas de Estacionamiento (Funciones sin Retorno)
# Enunciado: Un estacionamiento cobra según el tipo de vehículo: 1 = Auto ($1500/h), 2 = Camioneta ($2200/h), 3 = Moto ($800/h).

# Desarrollá una función llamada calcular_e_informar_ticket(tipo, horas) que reciba el tipo de vehículo y la cantidad de horas. La función debe hacer el cálculo y mostrar directamente por consola el detalle del ticket de forma prolija (no retorna nada).

# Para el programa principal (Script): Simulá el peaje/estacionamiento durante un día. Se ingresan vehículos hasta que el tipo sea 0. El programa principal debe acumular el dinero total recaudado del día (para esto, quizás te convenga que la función sí devuelva el monto, ¡decidilo vos!).

